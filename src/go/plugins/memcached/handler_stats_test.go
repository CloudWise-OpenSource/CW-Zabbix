package memcached

import (
	"errors"
	"reflect"
	"testing"

	"github.com/alimy/mc/v2"
)

func TestPlugin_statsHandler(t *testing.T) {
	fakeConn := stubConn{
		StatsFunc: func(key string) (mc.McStats, error) {
			switch key {
			case statsTypeGeneral:
				return mc.McStats{
					"pid":     "1234",
					"version": "1.4.15",
				}, nil

			case statsTypeSizes:
				return mc.McStats{
					"96": "1",
				}, nil

			case statsTypeSettings: // generates error for tests
				return nil, errors.New("some error")

			default:
				return nil, errors.New("unknown type")
			}
		},
		NoOpFunc: nil,
	}

	type args struct {
		conn   MCClient
		params []string
	}

	tests := []struct {
		name    string
		args    args
		want    interface{}
		wantErr error
	}{
		{
			name: "Should return error if wrong type specified",
			args: args{
				conn:   &fakeConn,
				params: []string{"wrong_type"},
			},
			want:    nil,
			wantErr: zabbixError{"unknown stats type"},
		},
		{
			name: "Should return error if cannot fetch data",
			args: args{
				conn:   &fakeConn,
				params: []string{statsTypeSettings},
			},
			want:    nil,
			wantErr: errorCannotFetchData,
		},
		{
			name: "General stats should be returned if type is not specified",
			args: args{
				conn:   &fakeConn,
				params: []string{},
			},
			want:    `{"pid":"1234","version":"1.4.15"}`,
			wantErr: nil,
		},
		{
			name: "Type should be passed to stats command if specified",
			args: args{
				conn:   &fakeConn,
				params: []string{statsTypeSizes},
			},
			want:    `{"96":"1"}`,
			wantErr: nil,
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got, err := statsHandler(tt.args.conn, tt.args.params)
			if !errors.Is(err, tt.wantErr) {
				t.Errorf("Plugin.statsHandler() error = %v, wantErr %v", err, tt.wantErr)
				return
			}

			if !reflect.DeepEqual(got, tt.want) {
				t.Errorf("Plugin.statsHandler() = %v, want %v", got, tt.want)
			}
		})
	}
}
